int	ft_str_is_printable(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != 0)
	{
		if (str[i] > 31 && str[i] < 127)
		{
			j++;
		}
		i++;
	}
	if (i == j)
		return (1);
	else
		return (0);
}
